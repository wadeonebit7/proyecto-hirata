    public void actualizarTodo() {
        // Usamos los métodos heredados de forma estructurada y segura
        if (Sesion.rol.equals("admin")) {
            tablaHistorial.setModel(ctrlAdmin.verHistorial(0)); // 0 porque no filtra
            tablaAlertas.setModel(ctrlAdmin.verAlertas(0));
            tablaUsuarios.setModel(ctrlAdmin.listarUsuarios());
            tablaCamiones.setModel(ctrlAdmin.listarCamiones());
        } else if (Sesion.rol.equals("conductor")) {
            tablaHistorial.setModel(ctrl.verHistorial(Sesion.idPersonal));
            tablaAlertas.setModel(ctrl.verAlertas(Sesion.idPersonal));
        }
    }
    
    private void actualizarEstadoToggleButton() {
        int fila = tablaUsuarios.getSelectedRow();
        
        // 1. Si no hay ninguna fila seleccionada, reseteamos el botón a un estado neutro
        if (fila == -1) {
            jToggleButton1.setText("Seleccione Usuario");
            jToggleButton1.setSelected(false);
            return;
        }
        
        // 2. Extraer el estado en texto de la columna 1 que creamos en ControladorAdmin ("Activo" / "Inactivo")
        String estadoActual = tablaUsuarios.getValueAt(fila, 1).toString();
        
        // 3. Sincronizar dinámicamente el texto y el estado hundido/libre del botón
        if (estadoActual.equalsIgnoreCase("Activo")) {
            jToggleButton1.setText("Desactivar Cuenta"); // Acción disponible
            jToggleButton1.setSelected(false);         // Estado LIBRE (Sin presionar)
        } else {
            jToggleButton1.setText("Activar Cuenta");   // Acción disponible
            jToggleButton1.setSelected(true);          // Estado PRESIONADO (Hundido)
        }
    }
    
