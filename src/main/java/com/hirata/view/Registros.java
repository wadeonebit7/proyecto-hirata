    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // 1. Validar que haya una fila seleccionada en la tabla de camiones
        int fila = tablaCamiones.getSelectedRow();
        String nuevoRut = rutAsig.getText().trim();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un camión de la tabla.");
            return;
        }

        if (nuevoRut.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el RUT del nuevo conductor.");
            return;
        }

        // 2. Obtener el ID del camión (asumiendo columna 0)
        int idCamion = (int) tablaCamiones.getValueAt(fila, 0);

        // 3. Ejecutar la reasignación mediante el controlador
        if (ctrlAdmin.reasignarConductor(idCamion, nuevoRut)) {
            JOptionPane.showMessageDialog(this, "Conductor reasignado exitosamente.");

            // 4. Limpiar campo y refrescar la tabla para ver el nuevo nombre
            rutAsig.setText("");
            tablaCamiones.setModel(ctrlAdmin.listarCamiones()); 
        } else {
            JOptionPane.showMessageDialog(this, "Error: El RUT no existe o el personal está inactivo.", 
                    "Error de validación", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        int fila = tablaUsuarios.getSelectedRow();

        if (fila != -1) {
            int idUsuario = (int) tablaUsuarios.getValueAt(fila, 0);
            
            if (idUsuario == com.hirata.model.Sesion.idPersonal) {
                JOptionPane.showMessageDialog(this, 
                    "Operación rechazada: No puedes desactivar la cuenta con la que estás firmado actualmente.", 
                    "Seguridad del Sistema", 
                    JOptionPane.WARNING_MESSAGE);

                // Forzamos al botón a regresar a su estado visual correcto sin mutar la BD
                actualizarEstadoToggleButton();
                return;
            }
            
            String estadoActual = tablaUsuarios.getValueAt(fila, 1).toString();
            boolean nuevoEstado = !estadoActual.equalsIgnoreCase("Activo");

            // Enviamos el cambio al controlador jerárquico
            if (ctrlAdmin.actualizarEstadoEntidad("usuarios", idUsuario, nuevoEstado)) {
                
                String mensaje = nuevoEstado ? "Usuario activado correctamente." : "Usuario desactivado correctamente.";
                
                JOptionPane.showMessageDialog(this, mensaje);
                
                actualizarTodo(); // Refrescamos los datos de la tabla
                
                actualizarEstadoToggleButton(); // Actualiza la apariencia del boton
            } else {
                JOptionPane.showMessageDialog(this, "Error al cambiar el estado del usuario.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un usuario de la tabla.");
            // Forzar el estado deseleccionado si hizo click sin fila
            jToggleButton1.setSelected(false);
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

