    // Método auxiliar interno para no repetir código al armar el Batch SQL
    private void agregarItemLote(PreparedStatement ps, int idOrden, String item, boolean esBueno) throws SQLException {
        ps.setInt(1, idOrden);
        ps.setString(2, item);
        ps.setBoolean(3, esBueno); // Guarda TRUE si el combo era 'Bueno', FALSE si era otra cosa
        ps.addBatch();
    }
    
    
    
    

