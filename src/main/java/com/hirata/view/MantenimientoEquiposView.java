    private void btnAbrirOrdenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirOrdenActionPerformed
        // TODO add your handling code here:
        String sn = txtNumeroSerie.getText().trim();
        String rutTecnico = txtTecnicoResponsable.getText().trim();
        String tipoMant = cmbTipoMantenimiento.getSelectedItem().toString().toLowerCase(); // 'preventivo' o 'correctivo'
        String motivo = txtMotivoIngreso.getText().trim();
        String firmaCliente = txtFirmaCliente.getText().trim(); // RUT del cliente

        // 1. Validaciones básicas de campos vacíos (Flujo Alterno)
        if (sn.isEmpty() || rutTecnico.isEmpty() || motivo.isEmpty() || firmaCliente.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos de texto antes de guardar.", "Datos Incompletos", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 2. Intentar abrir la Orden de Trabajo a través del Controlador
        // Usamos el ID del técnico en sesión como creador y el RUT ingresado para el responsable
        int idTecnicoResponsable = com.hirata.model.Sesion.idPersonal; 
        int idCreadorAutenticado = com.hirata.model.Sesion.idPersonal;

        int idOrdenActual = ctrlEquipos.abrirOrdenTrabajo(tipoMant, firmaCliente, motivo, sn, idTecnicoResponsable, idCreadorAutenticado);

        if (idOrdenActual == -1) {
            // Excepción oficial del documento: Equipo no registrado
            JOptionPane.showMessageDialog(this, "El número de serie '" + sn + "' no existe. Debe registrar el equipo primero.", "Excepción RF-06", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 3. Si la orden se creó con éxito, guardamos el Checklist de Recepción (Masivo)
        try (Connection con = com.hirata.model.MySQLConexion.getConexion()) {
            String sqlCheck = "INSERT INTO checklist_recepcion (id_orden, item, valor) VALUES (?, ?, ?)";

            try (PreparedStatement ps = con.prepareStatement(sqlCheck)) {

                // --- PARTE 1: ESTADO FÍSICO ---
                // Evaluamos si el combo está en "Bueno" (true), de lo contrario es false
                agregarItemLote(ps, idOrdenActual, "Carcasa sin golpes", cmbCarcasa.getSelectedItem().toString().equalsIgnoreCase("Bueno"));
                agregarItemLote(ps, idOrdenActual, "Pantalla sin rayaduras", cmbPantalla.getSelectedItem().toString().equalsIgnoreCase("Bueno"));
                agregarItemLote(ps, idOrdenActual, "Teclado completo", cmbTeclado.getSelectedItem().toString().equalsIgnoreCase("Bueno"));
                agregarItemLote(ps, idOrdenActual, "Touchpad funcional", cmbTouchPad.getSelectedItem().toString().equalsIgnoreCase("Bueno"));
                agregarItemLote(ps, idOrdenActual, "Puertos USB operativos", cmbPuertosUSB.getSelectedItem().toString().equalsIgnoreCase("Bueno"));
                agregarItemLote(ps, idOrdenActual, "Bandejas en buen estado", cmbBandejas.getSelectedItem().toString().equalsIgnoreCase("Bueno"));
                agregarItemLote(ps, idOrdenActual, "Panel/Botones funcionales", cmbPanelBotones.getSelectedItem().toString().equalsIgnoreCase("Bueno"));

                // --- PARTE 2: ESTADO FUNCIONAL ---
                agregarItemLote(ps, idOrdenActual, "Enciende correctamente", cmbEnciende.getSelectedItem().toString().equalsIgnoreCase("Bueno"));
                agregarItemLote(ps, idOrdenActual, "Inicia SO", cmbIniciaSO.getSelectedItem().toString().equalsIgnoreCase("Bueno"));
                agregarItemLote(ps, idOrdenActual, "Imagen operativa", cmbImagen.getSelectedItem().toString().equalsIgnoreCase("Bueno"));
                agregarItemLote(ps, idOrdenActual, "Impresión funcional", cmbImprime.getSelectedItem().toString().equalsIgnoreCase("Bueno"));
                agregarItemLote(ps, idOrdenActual, "Red/Wifi operativa", cmbRedWifi.getSelectedItem().toString().equalsIgnoreCase("Bueno"));

                // 1. Ejecutamos el lote en MySQL
                ps.executeBatch();

                // 2. Avisamos al usuario del éxito
                JOptionPane.showMessageDialog(this, "¡Mantenimiento Registrado!\nOrden de Trabajo #" + idOrdenActual + " guardada con éxito.");

                // 3. ¡AQUÍ ESTÁ LA MAGIA DEL FLUJO!
                if (dashboardPadre != null) {
                    dashboardPadre.actualizarTablaOrdenes(); // Le ordena al Dashboard recargar la tabla
                }

                this.dispose(); // Destruye y cierra esta ventana de la Ficha automáticamente

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al guardar el checklist en la base de datos: " + e.getMessage(), "Error BD", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAbrirOrdenActionPerformed

