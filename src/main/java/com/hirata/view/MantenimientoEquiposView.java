    private void txtNumeroSerieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtNumeroSerieActionPerformed
        // TODO add your handling code here:
        String sn = txtNumeroSerie.getText().trim();

        // Llamamos al controlador para verificar la existencia del equipo
        com.hirata.model.EquipoOficina equipo = ctrlEquipos.verificarYBuscarEquipo(sn);

        if (equipo != null) {
            // Si existe, mostramos sus datos en labels para darle feedback al técnico
            txtMarcaRight.setText(equipo.getMarca());
            txtModeloRight.setText(equipo.getModelo());
            btnAbrirOrden.setEnabled(true); // Habilitamos el flujo de apertura
        } else {
            // Excepción oficial del caso de estudio (RF-06)
            JOptionPane.showMessageDialog(this, 
                "El equipo no está registrado en el sistema. Debe registrarse primero.", 
                "Equipo No Encontrado", 
                JOptionPane.WARNING_MESSAGE);
            btnAbrirOrden.setEnabled(false);
        }
    }//GEN-LAST:event_txtNumeroSerieActionPerformed

