    private void cargarDatosInicialesEquipo() {
        // 1. Cargar componentes actuales del equipo
        DefaultTableModel modelComp = new DefaultTableModel(new String[]{"ID", "S/N", "Categoría", "Marca"}, 0);
        String sqlComp = "SELECT c.id, c.sn, cat.nombre, c.marca FROM equipo_componentes ec " +
                         "JOIN componentes c ON ec.id_componentes = c.id " +
                         "JOIN categoria_comp cat ON c.id_categoria_comp = cat.id WHERE ec.id_equipo = ?";
        try (Connection con = MySQLConexion.getConexion();
             PreparedStatement ps = con.prepareStatement(sqlComp)) {
            ps.setInt(1, idEquipoActual);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                modelComp.addRow(new Object[]{rs.getInt(1), rs.getString(2), rs.getString(3), rs.getString(4)});
            }
            tblComponentesActuales.setModel(modelComp);
        } catch (SQLException e) { e.printStackTrace(); }

        // 2. Cargar software actual del equipo
        DefaultTableModel modelSoft = new DefaultTableModel(new String[]{"ID EqSoft", "Nombre", "Versión Actual"}, 0);
        String sqlSoft = "SELECT id, nombre, version_actual FROM equipo_software WHERE id_equipo = ?";
        try (Connection con = MySQLConexion.getConexion();
             PreparedStatement ps = con.prepareStatement(sqlSoft)) {
            ps.setInt(1, idEquipoActual);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                modelSoft.addRow(new Object[]{rs.getInt(1), rs.getString(2), rs.getString(3)});
            }
            tblSoftwareActual.setModel(modelSoft);
        } catch (SQLException e) { e.printStackTrace(); }
    }

