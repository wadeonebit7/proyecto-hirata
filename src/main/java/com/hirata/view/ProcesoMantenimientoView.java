    private void btnBuscarCompatiblesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCompatiblesActionPerformed
        // TODO add your handling code here:
        int fila = tblComponentesActuales.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un componente de la lista actual.");
            return;
        }
        String categoria = tblComponentesActuales.getValueAt(fila, 2).toString();
        cmbRepuestosDisponibles.removeAllItems();

        // Busca repuestos en bodega que pertenezcan a la misma categoría
        String sql = "SELECT c.id, c.sn FROM componentes c JOIN categoria_comp cc ON c.id_categoria_comp = cc.id " +
                     "WHERE cc.nombre = ? AND c.estado = 'disponible'";
        try (Connection con = MySQLConexion.getConexion();
             PreparedStatement ps = con.prepareStatement(sql)) {
            ps.setString(1, categoria);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                cmbRepuestosDisponibles.addItem(rs.getInt("id") + " - " + rs.getString("sn"));
            }
        } catch (SQLException e) { e.printStackTrace(); }
    }//GEN-LAST:event_btnBuscarCompatiblesActionPerformed

